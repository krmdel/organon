_TEXT_FRAGMENT_MAX_CHARS = 100


def resolve_deep_link_url(
    entry: dict,
    quote: str = "",
    source_anchor: str = "",
) -> str:
    """Return a URL that lands the reader on the specific passage.

    Priority:
      1. paperclip anchor  (citations.gxl.ai/...#L<n>) — already exact
      2. DOI + #:~:text=<snippet>  (Chromium text fragment)
      3. arXiv + #:~:text=<snippet>
      4. plain DOI / arXiv URL (no fragment) — last resort

    Text fragment spec: https://wicg.github.io/scroll-to-text-fragment/
    Not all browsers support it, but it degrades to the base URL gracefully.
    PDF links won't honour it — documented limitation (see PLAN-V2 Tier-F risk 3).
    """
    if source_anchor and "citations.gxl.ai" in source_anchor:
        return source_anchor

    snippet = ""
    if quote:
        # Take the first ~100 chars, stop at a word boundary
        raw = quote.strip()[:_TEXT_FRAGMENT_MAX_CHARS]
        last_space = raw.rfind(" ")
        if last_space > 20:
            raw = raw[:last_space]
        snippet = urllib.parse.quote(raw, safe="")

    doi = (entry.get("doi") or "").strip()
    eprint = (entry.get("eprint") or "").strip()

    if doi:
        base = f"https://doi.org/{doi}"
        return f"{base}#:~:text={snippet}" if snippet else base
    if eprint:
        base = f"https://arxiv.org/abs/{eprint}"
        return f"{base}#:~:text={snippet}" if snippet else base

    url = (entry.get("url") or "").strip()
    return url


def format_bibliography_with_deep_links(
    entries: list[dict],
    style: str,
    sidecar: dict | None = None,
) -> str:
    """Generate a references section where each entry URL is a deep link.

    Each entry gets a markdown hyperlink using resolve_deep_link_url so the
    reader clicks directly to the cited passage rather than the paper root.

    Args:
        entries: List of entry dicts from parse_bib_file.
        style: One of 'apa', 'nature', 'ieee', 'vancouver'.
        sidecar: Optional parsed citations sidecar dict (from
            load_citations_sidecar). When present, the sidecar quote and
            source_anchor for each key inform the fragment URL.

    Returns:
        Formatted bibliography as a markdown string with ## References header.
    """
    claims_by_key: dict[str, dict] = {}
    if sidecar:
        for claim in sidecar.get("claims", []):
            k = (claim.get("key") or "").strip().lower()
            if k:
                claims_by_key[k] = claim

    def sort_key(e: dict) -> str:
        parts = _parse_author_parts(e.get("author", "ZZZ"))
        return _get_last_name(parts[0]).lower() if parts else "zzz"

    sorted_entries = sorted(entries, key=sort_key)
    numbered = style.lower() in ("nature", "ieee", "vancouver")

    lines = ["## References", ""]
    for i, entry in enumerate(sorted_entries, 1):
        key = (entry.get("key") or "").lower()
        claim = claims_by_key.get(key, {})
        quote = (claim.get("quote") or "").strip()
        source_anchor = (claim.get("source_anchor") or "").strip()

        citation_text = format_citation(entry, style)
        deep_url = resolve_deep_link_url(entry, quote=quote, source_anchor=source_anchor)

        if deep_url:
            # Wrap the URL portion as a markdown link: [citation text](url)
            entry_str = f"[{citation_text}]({deep_url})"
        else:
            entry_str = citation_text

        if numbered:
            lines.append(f"[{i}] {entry_str}")
        else:
            lines.append(entry_str)

    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Citation Marker Replacement
# ---------------------------------------------------------------------------


def replace_citation_markers(
    text: str, entries: list[dict], style: str
) -> tuple[str, list[str]]:
    """Replace [@key] citation markers in text with formatted citations.

    Handles single markers [@Key] and multi-cite markers [@Key1; @Key2].
    Key matching is case-insensitive. Unmatched keys generate warnings.

    Args:
        text: Draft text containing [@key] markers.
        entries: List of entry dicts from parse_bib_file.
        style: One of 'apa', 'nature', 'ieee', 'vancouver'.

    Returns:
        Tuple of (modified_text, list_of_warnings).
    """
    warnings: list[str] = []

    # Build case-insensitive key lookup and assign citation numbers
    # Sort entries alphabetically for consistent numbering
    def sort_key(entry: dict) -> str:
        author_str = entry.get("author", "ZZZ")
        parts = _parse_author_parts(author_str)
        if parts:
            return _get_last_name(parts[0]).lower()
        return "zzz"

    sorted_entries = sorted(entries, key=sort_key)
    key_map: dict[str, dict] = {}
    number_map: dict[str, int] = {}
    for i, entry in enumerate(sorted_entries, 1):
        key_lower = entry["key"].lower()
        key_map[key_lower] = entry
        number_map[key_lower] = i

    # Pattern: [@key] or [@key1; @key2; ...]
    marker_pattern = re.compile(r"\[@([^\]]+)\]")

    def replace_match(match: re.Match) -> str:
        inner = match.group(1)
        keys = [k.strip().lstrip("@") for k in inner.split(";")]

        resolved = []
        for key in keys:
            key_lower = key.lower()
            if key_lower in key_map:
                entry = key_map[key_lower]
                num = number_map[key_lower]
                resolved.append((entry, num))
            else:
                warnings.append(f"Unmatched citation key: {key}")

        if not resolved:
            return match.group(0)  # Leave unmatched markers as-is

        style_lower = style.lower()

        if style_lower == "apa":
            # APA multi-cite: "(Author1, Year1; Author2, Year2)"
            parts = []
            for entry, num in resolved:
                intext = format_intext(entry, "apa")
                # Strip outer parens for combining
                parts.append(intext[1:-1])
            return "(" + "; ".join(parts) + ")"

        elif style_lower == "nature":
            # Nature multi-cite: "[1,2]"
            nums = [str(num) for _, num in resolved]
            return "[" + ",".join(nums) + "]"

        elif style_lower == "ieee":
            # IEEE multi-cite: "[1], [2]"
            return ", ".join(f"[{num}]" for _, num in resolved)

        elif style_lower == "vancouver":
            # Vancouver multi-cite: "(1,2)"
            nums = [str(num) for _, num in resolved]
            return "(" + ",".join(nums) + ")"

        return match.group(0)

    result = marker_pattern.sub(replace_match, text)
    return result, warnings


# ---------------------------------------------------------------------------
# Reproducibility Logging
# ---------------------------------------------------------------------------


def log_citation_operation(
    operation: str,
    params: dict,
    bib_path: str = "",
    output_path: str = "",
) -> dict:
    """Log a citation formatting operation to the reproducibility ledger.

    Args:
        operation: Name of the operation (e.g., 'format_bibliography').
        params: Parameters used.
        bib_path: Path to .bib input file.
        output_path: Path to output file if any.

    Returns:
        The logged entry dict.
    """
    data_files = [bib_path] if bib_path and Path(bib_path).exists() else None
    output_files = [output_path] if output_path else None

    return log_operation(
        skill="sci-writing",
        operation=operation,
        params=params,
        data_files=data_files,
        output_files=output_files,
    )
